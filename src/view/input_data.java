    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        // TODO add your handling code here:
        int row = jTable.getSelectedRow();
        txtnik.setText(jTable.getModel().getValueAt(row, 0).toString());
          txtnama.setText(jTable.getModel().getValueAt(row, 1).toString());
            txtjk.setText(jTable.getModel().getValueAt(row, 2).toString());
              txtalamat.setText(jTable.getModel().getValueAt(row, 3).toString());
                txtjabatan.setText(jTable.getModel().getValueAt(row, 4).toString());
                  txtno.setText(jTable.getModel().getValueAt(row, 5).toString());
                    txtemail.setText(jTable.getModel().getValueAt(row, 6).toString());
    }//GEN-LAST:event_jTableMouseClicked

