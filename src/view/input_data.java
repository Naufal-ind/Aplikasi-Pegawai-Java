        stm.nik = txtnik.getText();
        stm.nama = txtnama.getText();
        stm.jenis_kelamin = txtjk.getText();
        stm.alamat = txtalamat.getText();
        stm.jabatan = txtjabatan.getText();
        stm.no_telp = txtno.getText();
        stm.email = txtemail.getText();
